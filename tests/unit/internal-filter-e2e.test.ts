describe("Integration: Simulated smart.click recording", () => {
  /**
   * Simulate what happens when smart.click("a") is called during a recording.
   * The bridge receives:
   *   1. N × browser_evaluate (_internal: true) — from pollActionability
   *   2. 1 × browser_click (_internal: absent) — the actual click
   *
   * We replay these commands through the recording guard and verify counts.
   */
  it("smart.click generates 1 recorded interaction, not 10+", async () => {
    const allCommands: Array<{ type: string; _internal?: boolean; [k: string]: unknown }> = [];

    // Phase 1: Collect commands from pollActionability
    const bridge = {
      send: vi.fn(async (cmd: any) => {
        allCommands.push({ ...cmd });
        return { actionable: true };
      }),
      isConnected: true,
      push: vi.fn(),
    };

    await pollActionability(bridge as any, "a", 1000);

    // Phase 2: Add the actual browser_click (no _internal flag)
    allCommands.push({ type: "browser_click", selector: "a" });

    // Phase 3: Replay through recording guard
    const recorded: typeof allCommands = [];
    for (const cmd of allCommands) {
      if (shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)) {
        recorded.push(cmd);
      }
    }

    // Evidence: multiple commands were generated total
    expect(allCommands.length).toBeGreaterThan(1);

    // Evidence: only the actual click was recorded
    expect(recorded).toHaveLength(1);
    expect(recorded[0].type).toBe("browser_click");
    expect(recorded[0]._internal).toBeUndefined();
  });

  it("smart.type generates 1 recorded interaction, not 10+", async () => {
    const allCommands: Array<{ type: string; _internal?: boolean; [k: string]: unknown }> = [];

    const bridge = {
      send: vi.fn(async (cmd: any) => {
        allCommands.push({ ...cmd });
        return { actionable: true };
      }),
      isConnected: true,
      push: vi.fn(),
    };

    await pollActionability(bridge as any, "input#email", 1000);

    // Add the actual browser_type (no _internal flag)
    allCommands.push({ type: "browser_type", selector: "input#email", text: "test@example.com" });

    const recorded = allCommands.filter(cmd =>
      shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)
    );

    expect(recorded).toHaveLength(1);
    expect(recorded[0].type).toBe("browser_type");
  });

  it("user-initiated browser_evaluate IS still recorded", async () => {
    // User calls execute tool with an expression — no _internal flag
    const userEvaluate = { type: "browser_evaluate", expression: "document.title" };
    expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(userEvaluate.type), userEvaluate)).toBe(true);
  });

  it("10 sequential polling evaluates + 1 click = exactly 1 recording", () => {
    // Simulate worst case: 10 polling retries before click
    const commands: Array<Record<string, unknown>> = [];

    for (let i = 0; i < 10; i++) {
      commands.push({
        type: "browser_evaluate",
        _internal: true,
        expression: `(() => { const el = document.querySelector("a"); ... })()`,
      });
    }
    commands.push({ type: "browser_click", selector: "a" });

    const recorded = commands.filter(cmd =>
      shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type as string), cmd)
    );

    expect(commands).toHaveLength(11);
    expect(recorded).toHaveLength(1);
    expect(recorded[0].type).toBe("browser_click");
  });
});

