// Failure path: _internal does NOT break command execution
// ============================================================

describe("Failure path: _internal does not break command execution", () => {
  it("checkActionability returns actionable result despite _internal flag", async () => {
    const { bridge } = createCapturingBridge({ actionable: true });
    const result = await checkActionability(bridge as any, "#btn");
    expect(result.actionable).toBe(true);
  });

  it("checkActionability returns non-actionable result despite _internal flag", async () => {
    const { bridge } = createCapturingBridge({ actionable: false, reason: "Element not found" });
    const result = await checkActionability(bridge as any, "#missing");
    expect(result.actionable).toBe(false);
    expect(result.reason).toBe("Element not found");
  });

  it("internal commands still fall through to handleCommand", () => {
    // The guard at line 2244 returns early for recorded commands.
    // If the guard doesn't match (because _internal is true), execution
    // falls through to line 2278: return handleCommand(command);
    // This means internal commands still execute — they just aren't recorded.
    const cmd = { type: "browser_evaluate", _internal: true, expression: "1+1" };
    const wouldRecord = shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd);
    expect(wouldRecord).toBe(false);
    // Falls through to handleCommand — command executes normally
  });
});
