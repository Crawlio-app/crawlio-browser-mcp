// Layer 1b: pollActionability sends MULTIPLE _internal commands
// ============================================================

describe("Layer 1b: pollActionability generates multiple internal commands", () => {
  it("all retry attempts are marked _internal: true", async () => {
    let callCount = 0;
    const bridge = {
      send: vi.fn(async (cmd: any) => {
        callCount++;
        // Fail first 3, succeed on 4th
        if (callCount < 4) return { actionable: false, reason: "Element not found" };
        return { actionable: true };
      }),
      isConnected: true,
      push: vi.fn(),
    };

    await pollActionability(bridge as any, "#delayed", 5000);

    expect(callCount).toBe(4);
    for (const call of bridge.send.mock.calls) {
      expect(call[0]._internal).toBe(true);
    }
  });

  it("zero user-visible commands are generated by polling", async () => {
    const allCommands: any[] = [];
    const bridge = {
      send: vi.fn(async (cmd: any) => {
        allCommands.push(cmd);
        return { actionable: true };
      }),
      isConnected: true,
      push: vi.fn(),
    };

    await pollActionability(bridge as any, "button", 1000);

    // Every single command must be internal
    const nonInternal = allCommands.filter(c => !c._internal);
    expect(nonInternal).toHaveLength(0);
  });
});

// ============================================================
// Layer 2: Recording guard filters _internal commands
// ============================================================

describe("Layer 2: Recording guard predicate", () => {
  describe("commands that SHOULD be recorded", () => {
    it("browser_click without _internal", () => {
      const cmd = { type: "browser_click", selector: "#btn" };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(true);
    });

    it("browser_evaluate without _internal (user-initiated)", () => {
      const cmd = { type: "browser_evaluate", expression: "document.title" };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(true);
    });

    it("browser_navigate without _internal", () => {
      const cmd = { type: "browser_navigate", url: "https://example.com" };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(true);
    });

    it("browser_type without _internal", () => {
      const cmd = { type: "browser_type", selector: "input", text: "hello" };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(true);
    });

    it("browser_fill_form without _internal", () => {
      const cmd = { type: "browser_fill_form", selector: "form", values: {} };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(true);
    });
  });

  describe("commands that SHOULD NOT be recorded (internal polling)", () => {
    it("browser_evaluate with _internal: true", () => {
      const cmd = { type: "browser_evaluate", _internal: true, expression: "(() => { ... })()" };
      expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(false);
    });

    it("any interaction tool with _internal: true is filtered", () => {
      for (const tool of RECORDING_INTERACTION_TOOLS) {
        const cmd = { type: tool, _internal: true };
        expect(shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(tool), cmd)).toBe(false);
      }
    });
  });

  describe("commands that skip recording (not interaction tools)", () => {
    it("capture_page is not in RECORDING_INTERACTION_TOOLS", () => {
      expect(RECORDING_INTERACTION_TOOLS.has("capture_page")).toBe(false);
      const cmd = { type: "capture_page" };
      expect(shouldRecord(true, false, cmd)).toBe(false);
    });

    it("take_screenshot is not in RECORDING_INTERACTION_TOOLS", () => {
      expect(RECORDING_INTERACTION_TOOLS.has("take_screenshot")).toBe(false);
    });

    it("get_dom_snapshot is not in RECORDING_INTERACTION_TOOLS", () => {
      expect(RECORDING_INTERACTION_TOOLS.has("get_dom_snapshot")).toBe(false);
    });
  });

  describe("no recording active — nothing is recorded", () => {
    it("regular command with no active recording", () => {
      const cmd = { type: "browser_click", selector: "#btn" };
      expect(shouldRecord(false, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(false);
    });

    it("internal command with no active recording", () => {
      const cmd = { type: "browser_evaluate", _internal: true, expression: "..." };
      expect(shouldRecord(false, RECORDING_INTERACTION_TOOLS.has(cmd.type), cmd)).toBe(false);
    });
  });
});

