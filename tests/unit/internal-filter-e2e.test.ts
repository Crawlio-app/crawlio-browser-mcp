// Layer 2b: Edge cases for _internal flag
// ============================================================

describe("Layer 2b: _internal edge cases", () => {
  it("_internal: false does NOT filter (treated as user command)", () => {
    const cmd = { type: "browser_evaluate", _internal: false, expression: "x" };
    // !false === true, so this passes the guard
    expect(shouldRecord(true, true, cmd)).toBe(true);
  });

  it("_internal: undefined does NOT filter (normal user command)", () => {
    const cmd = { type: "browser_evaluate", expression: "x" };
    // !undefined === true
    expect(shouldRecord(true, true, cmd)).toBe(true);
  });

  it("_internal: null does NOT filter (falsy, treated as user command)", () => {
    const cmd = { type: "browser_evaluate", _internal: null, expression: "x" };
    // !null === true
    expect(shouldRecord(true, true, cmd)).toBe(true);
  });

  it("_internal: 0 does NOT filter (falsy number)", () => {
    const cmd = { type: "browser_evaluate", _internal: 0, expression: "x" };
    // !0 === true
    expect(shouldRecord(true, true, cmd)).toBe(true);
  });

  it("_internal: 'true' (string) DOES filter (truthy)", () => {
    const cmd = { type: "browser_evaluate", _internal: "true", expression: "x" };
    // !"true" === false
    expect(shouldRecord(true, true, cmd)).toBe(false);
  });

  it("_internal: 1 DOES filter (truthy number)", () => {
    const cmd = { type: "browser_evaluate", _internal: 1, expression: "x" };
    // !1 === false
    expect(shouldRecord(true, true, cmd)).toBe(false);
  });
});

