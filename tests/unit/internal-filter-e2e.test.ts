/**
 * E2E Black-Box Test: Recording Interaction Noise Fix
 *
 * Validates that internal polling commands (_internal: true) are excluded
 * from session recordings, while user-initiated commands are still recorded.
 *
 * Tests both layers as black boxes:
 *   Layer 1 (MCP tools.ts): checkActionability sends _internal: true
 *   Layer 2 (background.ts): recording interceptor filters _internal commands
 *
 * Since background.ts is an IIFE (not importable), Layer 2 is tested by
 * extracting the exact guard logic and replaying real command sequences.
 */

import { describe, it, expect, vi } from "vitest";
import { checkActionability, pollActionability, ACTIONABILITY_BACKOFF } from "@/mcp-server/tools";

// ============================================================
// Helpers
// ============================================================

/** Capture every command sent through bridge.send */
function createCapturingBridge(response: unknown = { actionable: true }) {
  const captured: Array<{ cmd: any; timeout?: number }> = [];
  return {
    captured,
    bridge: {
      send: vi.fn(async (cmd: any, timeout?: number) => {
        captured.push({ cmd, timeout });
        return response;
      }),
      isConnected: true,
      push: vi.fn(),
    },
  };
}

/**
 * Replicate the EXACT recording guard from background.ts line 2244.
 * This is the single line we changed — test it as a predicate.
 */
function shouldRecord(
  activeRecording: boolean,
  toolInSet: boolean,
  command: Record<string, unknown>,
): boolean {
  // Original (before fix): activeRecording && toolInSet
  // Fixed:                  activeRecording && toolInSet && !command._internal
  return activeRecording && toolInSet && !command._internal;
}

/** The 12 interaction tools from background.ts line 531-536 */
const RECORDING_INTERACTION_TOOLS = new Set([
  "browser_navigate", "browser_click", "browser_type", "browser_press_key",
  "browser_hover", "browser_select_option", "browser_scroll",
  "browser_double_click", "browser_drag", "browser_fill_form",
  "browser_evaluate", "browser_file_upload",
]);

// ============================================================
// Layer 1: checkActionability sends _internal: true
// ============================================================

describe("Layer 1: checkActionability marks commands as internal", () => {
  it("bridge.send receives _internal: true", async () => {
    const { bridge, captured } = createCapturingBridge({ actionable: true });
    await checkActionability(bridge as any, "#btn");

    expect(captured).toHaveLength(1);
    expect(captured[0].cmd._internal).toBe(true);
  });

  it("command type is browser_evaluate", async () => {
    const { bridge, captured } = createCapturingBridge({ actionable: true });
    await checkActionability(bridge as any, "a.link");

    expect(captured[0].cmd.type).toBe("browser_evaluate");
  });

  it("expression field is present and non-empty", async () => {
    const { bridge, captured } = createCapturingBridge({ actionable: true });
    await checkActionability(bridge as any, "div");

    expect(typeof captured[0].cmd.expression).toBe("string");
    expect(captured[0].cmd.expression.length).toBeGreaterThan(0);
  });

  it("selector is embedded in the expression", async () => {
    const { bridge, captured } = createCapturingBridge({ actionable: true });
    await checkActionability(bridge as any, "#my-special-btn");

    expect(captured[0].cmd.expression).toContain("#my-special-btn");
  });

  it("_internal is strictly boolean true, not truthy string", async () => {
    const { bridge, captured } = createCapturingBridge({ actionable: true });
    await checkActionability(bridge as any, "a");

    expect(captured[0].cmd._internal).not.toBe("true");
    expect(captured[0].cmd._internal).toBe(true);
  });
});

