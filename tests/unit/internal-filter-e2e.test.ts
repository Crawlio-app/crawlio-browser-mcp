describe("Serialization: _internal survives JSON round-trip", () => {
  it("JSON.stringify preserves _internal: true", () => {
    const cmd = { type: "browser_evaluate", _internal: true, expression: "..." };
    const serialized = JSON.stringify(cmd);
    const deserialized = JSON.parse(serialized);

    expect(deserialized._internal).toBe(true);
    expect(typeof deserialized._internal).toBe("boolean");
  });

  it("JSON.parse of command without _internal has undefined", () => {
    const cmd = { type: "browser_click", selector: "a" };
    const deserialized = JSON.parse(JSON.stringify(cmd));

    expect(deserialized._internal).toBeUndefined();
    // !undefined === true, so guard passes (command is recorded)
    expect(!deserialized._internal).toBe(true);
  });

  it("spread operator preserves _internal in recorded args", () => {
    // background.ts line 2258: const { type: _, id: _id, ...args } = command;
    const command = { type: "browser_evaluate", id: "uuid", _internal: true, expression: "..." };
    const { type: _, id: _id, ...args } = command;

    // _internal leaks into args — but that's fine because the command is filtered
    // before reaching the spread. Verify the spread behavior.
    expect(args._internal).toBe(true);
    expect(args.expression).toBe("...");
  });

  it("filtered internal commands never reach the args spread", () => {
    // Verify: if _internal is true, shouldRecord returns false,
    // so the spread at line 2258 is never reached.
    const command = { type: "browser_evaluate", _internal: true, expression: "..." };
    const wouldRecord = shouldRecord(true, RECORDING_INTERACTION_TOOLS.has(command.type), command);
    expect(wouldRecord).toBe(false);
    // The spread code at line 2258-2267 only executes if shouldRecord is true.
    // So _internal never appears in recorded interaction args.
  });
});

